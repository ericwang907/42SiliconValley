char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		dest[i] = src[i];
		i++;
	}
	if (n < ft_strlen(src))
	{
		while (src[i] != '\0')
		{
			src[i] = '\0';
			i++;
		}
	}
}
